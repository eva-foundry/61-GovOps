def _select_shapes(raw_shapes: Iterable[str] | None) -> list[str]:
    if raw_shapes is None:
        return list(SUPPORTED_SHAPES)
    selected = [s.strip().lower() for s in raw_shapes if s.strip()]
    unknown = [s for s in selected if s not in SUPPORTED_SHAPES]
    if unknown:
        raise InitError(
            f"Unknown shape(s): {unknown}. "
            f"Supported: {list(SUPPORTED_SHAPES)}"
        )
    return selected or list(SUPPORTED_SHAPES)


def init_jurisdiction(
    code: str,
    *,
    shapes: Iterable[str] | None = None,
    lawcode_dir: Path | None = None,
) -> list[Path]:
    """Generate a complete ``lawcode/<code>/`` skeleton.

    Returns the list of files written, in creation order. Refuses if any
    target file already exists — partial scaffolds would leave the
    directory in an ambiguous state.
    """
    norm_code = _normalize_country_code(code)
    selected = _select_shapes(shapes)
    base = (lawcode_dir or DEFAULT_LAWCODE_DIR) / norm_code

    targets: dict[Path, str] = {}
    targets[base / "jurisdiction.yaml"] = _jurisdiction_yaml(norm_code)
    if "oas" in selected:
        targets[base / "programs" / "oas.yaml"] = _oas_program_yaml(norm_code)
        targets[base / "config" / "oas-rules.yaml"] = _oas_config_yaml(norm_code)
    if "ei" in selected:
        targets[base / "programs" / "ei.yaml"] = _ei_program_yaml(norm_code)
        targets[base / "config" / "ei-rules.yaml"] = _ei_config_yaml(norm_code)

    _check_no_collisions(list(targets.keys()))

    written: list[Path] = []
    for path, content in targets.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        written.append(path)
        if path.suffix == ".yaml" and path.parent.name == "programs":
            doc = write_plain_language_doc(path)
            written.append(doc)
    return written


