# ---------------------------------------------------------------------------
# Plain-language doc generator (Phase H §"each manifest has a sibling .md")
# ---------------------------------------------------------------------------


def render_plain_language_doc(manifest_path: Path) -> str:
    """Produce a plain-language Markdown rendering of a program manifest.

    The intent is to give a non-coder program leader a readable summary of
    what the YAML actually encodes — every rule, citation, and demo case
    in prose. Run on every program manifest in the repo to maintain the
    Phase H convention; also called from ``govops init`` to seed the
    sidecar alongside a fresh manifest.
    """
    with manifest_path.open("r", encoding="utf-8") as fh:
        raw = yaml.safe_load(fh)
    if not isinstance(raw, dict):
        raise InitError(f"{manifest_path}: top-level must be a mapping.")

    program_id = raw.get("program_id", "?")
    jurisdiction_id = raw.get("jurisdiction_id", "?")
    shape = raw.get("shape", "?")
    name = raw.get("name", {}) or {}
    description = raw.get("description", {}) or {}
    authority = raw.get("authority_chain", []) or []
    rules = raw.get("rules", []) or []
    demos = raw.get("demo_cases", []) or []

    en_name = name.get("en") or next(iter(name.values()), program_id)
    en_desc = description.get("en") or next(iter(description.values()), "")

    lines: list[str] = []
    lines.append(f"# {en_name}")
    lines.append("")
    lines.append(
        "_This document is a plain-language rendering of the program manifest "
        f"(`{manifest_path.name}`) for non-coder review. The YAML next to it is "
        "the authoritative source the engine reads; this Markdown is generated "
        "from the same content and may be regenerated whenever the YAML "
        "changes._"
    )
    lines.append("")

    lines.append("## At a glance")
    lines.append("")
    lines.append(f"- **Program id**: `{program_id}`")
    lines.append(f"- **Jurisdiction**: `{jurisdiction_id}`")
    lines.append(f"- **Shape**: `{shape}`")
    if name:
        for lang, label in name.items():
            lines.append(f"- **Name ({lang})**: {label}")
    if en_desc:
        lines.append("")
        lines.append(en_desc)
    lines.append("")

    if authority:
        lines.append("## Authority chain")
        lines.append("")
        lines.append(
            "Where this program's authority comes from, top to bottom — "
            "constitution at the top, the specific service at the bottom."
        )
        lines.append("")
        for ref in authority:
            layer = ref.get("layer", "")
            title = ref.get("title", "")
            citation = ref.get("citation", "")
            lines.append(f"- **{layer}** — {title}  ")
            lines.append(f"  Citation: `{citation}`")
            url = ref.get("url")
            if url:
                lines.append(f"  Link: <{url}>")
        lines.append("")

    if rules:
        lines.append("## Rules the engine evaluates")
        lines.append("")
        lines.append(
            "Each rule is a condition the engine checks against a case. "
            "Parameter values come from the substrate "
            f"(`lawcode/.../config/{program_id}-rules.yaml`) and can be "
            "amended through the dual-approval workflow without touching "
            "this manifest."
        )
        lines.append("")
        for rule in rules:
            rule_id = rule.get("id", "?")
            rule_type = rule.get("rule_type", "?")
            desc = rule.get("description", "")
            citation = rule.get("citation", "")
            lines.append(f"### `{rule_id}` ({rule_type})")
            lines.append("")
            if desc:
                lines.append(f"> {desc}")
                lines.append("")
            if citation:
                lines.append(f"Citation: `{citation}`")
                lines.append("")
            params = rule.get("parameters") or {}
            if params:
                lines.append("Parameters (read from substrate at evaluation time):")
                lines.append("")
                for pname, pspec in params.items():
                    if isinstance(pspec, dict) and "ref" in pspec:
                        lines.append(f"- `{pname}` ← substrate key `{pspec['ref']}`")
                    elif isinstance(pspec, dict) and "include" in pspec:
                        lines.append(f"- `{pname}` ← include `{pspec['include']}`")
                    else:
                        lines.append(f"- `{pname}` = {pspec}")
                lines.append("")

    if demos:
        lines.append("## Demo cases")
        lines.append("")
        lines.append(
            "Synthetic applicants used by the test suite and the demo UI — "
            "no real personal data."
        )
        lines.append("")
        for case in demos:
            cid = case.get("id", "?")
            applicant = case.get("applicant") or {}
            label = applicant.get("legal_name") or "(unnamed)"
            status = applicant.get("legal_status", "?")
            lines.append(f"- **`{cid}`** — {label} ({status})")
        lines.append("")

    lines.append("---")
    lines.append("")
    lines.append(
        "Regenerate this file with `govops docs <manifest-path>` whenever the "
        "manifest changes."
    )
    lines.append("")
    return "\n".join(lines)


def write_plain_language_doc(manifest_path: Path) -> Path:
    """Write the sibling ``<id>.md`` for a manifest. Returns the doc path."""
    out = manifest_path.with_suffix(".md")
    text = render_plain_language_doc(manifest_path)
    out.write_text(text, encoding="utf-8")
    return out


