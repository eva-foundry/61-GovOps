"""``govops init`` — scaffold a new jurisdiction from canonical shape templates.

Phase H of v3 (per PLAN-v3 §Phase H): the adoption substrate. A non-engineer
program leader runs::

    govops init pl --shapes oas,ei

and gets a complete ``lawcode/pl/`` skeleton with TODO markers at every place
that needs jurisdiction-specific work. The skeleton is schema-valid the
moment it lands — every file passes the Phase 5 validator — so the
contributor's first ``pytest`` confirms the structure before they touch a
single statutory citation.

Refusal posture: the command never overwrites existing files. If any target
already exists, the entire scaffold is aborted with an error listing the
collisions, so accidentally re-running ``init`` over real work cannot
silently destroy it.
"""

from __future__ import annotations

import sys
from pathlib import Path
from typing import Iterable

import yaml


REPO_ROOT = Path(__file__).resolve().parent.parent.parent
DEFAULT_LAWCODE_DIR = REPO_ROOT / "lawcode"


SUPPORTED_SHAPES = ("oas", "ei")


# ---------------------------------------------------------------------------
# Errors
# ---------------------------------------------------------------------------


class InitError(RuntimeError):
    """Scaffold cannot be generated as requested."""


# ---------------------------------------------------------------------------
# Path helpers
# ---------------------------------------------------------------------------


def _normalize_country_code(raw: str) -> str:
    code = raw.strip().lower()
    if not code:
        raise InitError("Country code must not be empty.")
    if not code.isalpha() or len(code) > 6:
        raise InitError(
            f"Country code {raw!r} must be 1–6 ASCII letters (ISO-style)."
        )
    return code


def _check_no_collisions(targets: list[Path]) -> None:
    existing = [t for t in targets if t.exists()]
    if existing:
        rels = "\n  ".join(str(p) for p in existing)
        raise InitError(
            "Refusing to overwrite existing files. Remove them or pick a "
            f"different country code:\n  {rels}"
        )


