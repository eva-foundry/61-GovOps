def _run_init(
    *,
    country_code: str,
    shapes: list[str] | None,
    lawcode_dir: str | None,
) -> int:
    try:
        targets = init_jurisdiction(
            country_code,
            shapes=shapes,
            lawcode_dir=Path(lawcode_dir) if lawcode_dir else None,
        )
    except InitError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 2

    print()
    print(f"  Scaffolded {len(targets)} file(s) for `{country_code.lower()}`:")
    for t in targets:
        print(f"    {t}")
    print()
    print(
        "  Next steps:\n"
        "    1. Replace every TODO marker with jurisdiction-specific content.\n"
        "    2. Run `pytest -q` — schema validation runs on every push.\n"
        "    3. Add the new code to `JURISDICTION_REGISTRY` if you need it\n"
        "       to appear in the demo UI's jurisdiction dropdown.\n"
    )
    return 0


def _run_docs(*, manifest_path: str) -> int:
    path = Path(manifest_path)
    if not path.exists():
        print(f"error: manifest not found: {path}", file=sys.stderr)
        return 2
    try:
        out = write_plain_language_doc(path)
    except InitError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 2
    print(f"  Wrote {out}")
    return 0
